    private void selectCustomerComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectCustomerComboBoxActionPerformed
        // TODO add your handling code here:
        Customer selectedCustomer = (Customer)selectCustomerComboBox.getSelectedItem();
        
        firstNameTextField.setText(selectedCustomer.getCustomerFirstName());
        lastNameTextField.setText(selectedCustomer.getCustomerLastName());
        birthDateChooser.setDate(selectedCustomer.getBirthDate());
        phoneNumberTextField.setText(selectedCustomer.getPhoneNumber());
        emailAddressTextField.setText(selectedCustomer.getEmailAddress());
    }//GEN-LAST:event_selectCustomerComboBoxActionPerformed

    private void editCustomerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editCustomerButtonMouseClicked
        // TODO add your handling code here:
        Customer selectedCustomer = (Customer)selectCustomerComboBox.getSelectedItem();
        int customerID = selectedCustomer.getCustomerID();
        String customerFirstName = firstNameTextField.getText();
        String customerLastName = lastNameTextField.getText();
        String phoneNumber = phoneNumberTextField.getText();
        String emailAddress = emailAddressTextField.getText();
        Date birthDate = birthDateChooser.getDate();
        
        boolean customerUpdated = Customer.updateCustomerDetails(customerID, customerFirstName, customerLastName, birthDate, phoneNumber, emailAddress);
        
        String showMessage = "";
        if(customerUpdated){
            showMessage = "Customer details have been successfully updated!";
        }else{
            showMessage = "Oops! Some error occured while updating customer details!";
        }
        
        JOptionPane.showMessageDialog(null,showMessage);

        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_editCustomerButtonMouseClicked

