    private void populateCustomer() {
        customerComboBox.removeAllItems();
        //statesJComboBox.addItem("");
        //statesJComboBox.addItem("All States");

        try {
            ArrayList<Customer> customersList = Customer.getAllCustomers();
            for (Customer customer : customersList) {
                customerComboBox.addItem(customer);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    private void deleteCustomerButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteCustomerButtonMouseClicked
        // TODO add your handling code here:
        Customer selectedCustomer = (Customer) customerComboBox.getSelectedItem();
        int customerID = selectedCustomer.getCustomerID();

        boolean customerUpdated = Customer.deleteCustomer(customerID);
        String showMessage = "";
        if(customerUpdated){
            showMessage = "Selected Customer has been successfully deleted!";
        }else{
            showMessage = "Oops! Some error occured while deleting customer !";
        }

        JOptionPane.showMessageDialog(null,showMessage);

        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_deleteCustomerButtonMouseClicked

