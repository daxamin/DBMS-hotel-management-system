    private void populateRooms() {
        roomJComboBox.removeAllItems();
        //statesJComboBox.addItem("");
        //statesJComboBox.addItem("All States");

        try {
            ArrayList<Room> roomsList = Room.getAllRoomsList(selectedHotel.getHotelID());
            for (Room room : roomsList) {
                System.out.println(room.getRoomNo());
                roomJComboBox.addItem(room);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

