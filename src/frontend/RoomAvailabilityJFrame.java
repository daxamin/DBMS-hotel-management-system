    private void updateJButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateJButtonMouseClicked
        // TODO add your handling code here:
        Room selectedRoom = (Room) roomJComboBox.getSelectedItem();
        boolean availability = true;
        if(availableJRadioButton.isSelected()){
            availability = true;
        }else{
            availability = false;
        }
        
        Room.updateRoomAvailability(selectedRoom.getRoomNo(), selectedRoom.getHotelID(), availability);
        JOptionPane.showMessageDialog(null,"Selected Room has been successfully updated for availability!");
        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_updateJButtonMouseClicked

