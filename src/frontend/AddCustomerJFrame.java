    private void createCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createCustomerMouseClicked
        // TODO add your handling code here:
        String customerFirstName = firstNameJField.getText();
        String customerLastName = lastNameJField.getText();
        String phoneNumber = phoneNumberJField.getText();
        String emailAddress = emailAddressJField.getText();
        Date birthDate = birthDateChooser.getDateEditor().getDate();
        
        String showMessage = "";
        
        boolean createdCustomer = Customer.createCustomer(customerFirstName, customerLastName, birthDate, phoneNumber, emailAddress);
        
        if(createdCustomer)
            showMessage = "Successfully created new customer";
        else
            showMessage = "Oops! Some error occured while creating the new customer.";
        
        JOptionPane.showMessageDialog(null,showMessage);
        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_createCustomerMouseClicked

