    private void addPMButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addPMButtonMouseClicked
        // TODO add your handling code here:
        String paymentMethodName = PMnameTextField.getText();
        Double discountPercent = Double.parseDouble(discountTextField.getText());
        
        String showMessage = "";
        
        boolean createdPMMethod = PaymentMethod.createPaymentMethod(paymentMethodName, discountPercent);
        
        if(createdPMMethod)
            showMessage = "New Payment Method added successfully.";
        else
            showMessage = "Oops! Some Error occured when creating new payment method.";
        
        JOptionPane.showMessageDialog(null,showMessage);
        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_addPMButtonMouseClicked

