    private void removeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeMouseClicked
        // TODO add your handling code here:
        Room room = (Room)selectRoom.getSelectedItem();
        boolean roomUpdated = Room.deleteRoom(hotel.getHotelID(), room.getRoomNo());
        
        String showMessage = "";
        if(roomUpdated){
            showMessage = "Selected Room has been successfully removed!";
        }else{
            showMessage = "Oops! Some error occured while removing the room!";
        }

        JOptionPane.showMessageDialog(null,showMessage);

        WindowEvent winClosingEvent = new WindowEvent(this, WindowEvent.WINDOW_CLOSING);
        Toolkit.getDefaultToolkit().getSystemEventQueue().postEvent(winClosingEvent);
    }//GEN-LAST:event_removeMouseClicked

    
